export function t(key: TranslationKey): string {
  const { settings } = useStore.getState();
  const lang = settings.appLanguage || 'it';
  return translations[lang]?.[key] ?? translations['it'][key] ?? key;
}

// Hook per usare le traduzioni nei componenti React (forza il re-render quando cambia lingua)
export function useT() {
  const { settings } = useStore();
  // Usa settings come dipendenza per forzare il re-render al cambio lingua
  const _ = settings.appLanguage;
  return { t, lang: settings.appLanguage || 'it' };
}